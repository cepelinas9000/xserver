static void
parse_simple(void)
{
    xf86MonPtr mon =xf86InterpretEDID(0, samsung_q800t_hdmi2_0_edid);

    mon->flags |= MONITOR_EDID_COMPLETE_RAWDATA;

    xf86DoInterpretHDRMetadata(mon);

    assert(mon->hdr.colorimetry_profiles);
    assert(mon->hdr.hdr_valid);

    assert(mon->hdr.tf_4 == 0);
    assert(mon->hdr.tf_5 == 0);

    assert(mon->hdr.tf_tradinional_sdr == 1);
    assert(mon->hdr.tf_tradinional_hdr == 0);
    assert(mon->hdr.tf_pq == 1);
    assert(mon->hdr.tf_hlf == 1);

    assert(mon->hdr.desired_content_max_frame_avg_luminance == 0);
    assert(mon->hdr.desired_content_max_luminance == 0);
    assert(mon->hdr.desired_content_min_luminance == 0);

    assert(mon->hdr.colorimetry_profiles == 0x0301);
    assert(is_CTA_CDB_xvYCC601(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_xvYCC709(mon->hdr.colorimetry_profiles));
    free(mon);


    // msi_mag321curv_dp_edid
    mon =xf86InterpretEDID(0, msi_mag321curv_dp_edid);

    mon->flags |= MONITOR_EDID_COMPLETE_RAWDATA;

    xf86DoInterpretHDRMetadata(mon);

    assert(mon->hdr.colorimetry_profiles);
    assert(mon->hdr.hdr_valid);

    assert(mon->hdr.tf_4 == 0);
    assert(mon->hdr.tf_5 == 0);

    assert(mon->hdr.tf_tradinional_sdr == 1);
    assert(mon->hdr.tf_tradinional_hdr == 1);
    assert(mon->hdr.tf_pq == 1);
    assert(mon->hdr.tf_hlf == 0);

    assert(mon->hdr.desired_content_max_frame_avg_luminance == 366.801605f);
    assert(mon->hdr.desired_content_max_luminance == 366.801605f);
    assert(mon->hdr.desired_content_min_luminance == 0);


    assert(is_CTA_CDB_xvYCC601(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_xvYCC709(mon->hdr.colorimetry_profiles));

    assert(is_CTA_CDB_BT2020_cYCC(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_BT2020_YCC(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_BT2020_RGB(mon->hdr.colorimetry_profiles));

    assert(mon->hdr.colorimetry_profiles == 0xe301);


    free(mon);


    mon =xf86InterpretEDID(0, qemu_edid);

    mon->flags |= MONITOR_EDID_COMPLETE_RAWDATA;
    assert(xf86DoInterpretHDRMetadata(mon) == false);

    free(mon);


    // hp_5dq99aa_hdmi_edid
    mon =xf86InterpretEDID(0, hp_5dq99aa_hdmi_edid);

    mon->flags |= MONITOR_EDID_COMPLETE_RAWDATA;

    xf86DoInterpretHDRMetadata(mon);

    assert(mon->hdr.colorimetry_profiles);
    assert(mon->hdr.hdr_valid);

    assert(mon->hdr.tf_4 == 0);
    assert(mon->hdr.tf_5 == 0);

    assert(mon->hdr.tf_tradinional_sdr == 1);
    assert(mon->hdr.tf_tradinional_hdr == 1);
    assert(mon->hdr.tf_pq == 1);
    assert(mon->hdr.tf_hlf == 0);

    assert(mon->hdr.desired_content_max_frame_avg_luminance == 351.250427f);
    assert(mon->hdr.desired_content_max_luminance == 603.665771f);
    assert(mon->hdr.desired_content_min_luminance == 9.28359659e-05f);


    assert(mon->hdr.colorimetry_profiles == 0xe000);

    assert(is_CTA_CDB_xvYCC601(mon->hdr.colorimetry_profiles) == 0);
    assert(is_CTA_CDB_xvYCC709(mon->hdr.colorimetry_profiles) == 0);

    assert(is_CTA_CDB_BT2020_cYCC(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_BT2020_YCC(mon->hdr.colorimetry_profiles));
    assert(is_CTA_CDB_BT2020_RGB(mon->hdr.colorimetry_profiles));

    free(mon);




}

